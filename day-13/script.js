function labelcreate(tagname,attrname,attrvalue,content){
    var ele=document.createElement(tagname);
    ele.setAttribute(attrname,attrvalue);
    ele.innerHTML=content;
    return ele;
    }
    
    //line breaker
    function linebreakers(tagname){
    var ele=document.createElement(tagname);
    return ele;
    }
    //input elements
    function inputele(tagname,attrname,attrvalue,attrname1,attrvalue1,attrname2,attrvalue2){
    var ele=document.createElement(tagname);
    ele.setAttribute(attrname,attrvalue);
    ele.setAttribute(attrname1,attrvalue1);
    ele.setAttribute(attrname2,attrvalue2);
    return ele;
    }
var Firstname=labelcreate("label","for","First Name","First Name");
var br1=linebreakers("br");
var Firstnameinput=inputele("input","type","First Name", "Name", "First Name","id","First Name");
var br2=linebreakers("br");
document.body.append(Firstname,br1,Firstnameinput,br2);



